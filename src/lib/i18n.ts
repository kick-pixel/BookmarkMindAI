export type MessageKey = keyof typeof messages['zh-CN']

export function resolveLocale(language?: AppLanguage): Locale {
  if (language === 'zh-CN' || language === 'en') return language
  const browserLanguage =
    typeof chrome !== 'undefined' && chrome.i18n?.getUILanguage
      ? chrome.i18n.getUILanguage()
      : navigator.language
  return browserLanguage?.toLowerCase().startsWith('zh') ? 'zh-CN' : 'en'
}

export function translate(locale: Locale, key: MessageKey, values?: Record<string, string | number>): string {
  let message: string = messages[locale][key] ?? messages['zh-CN'][key] ?? key
  if (!values) return message
  for (const [name, value] of Object.entries(values)) {
    message = message.replaceAll(`{${name}}`, String(value))
  }
  return message
}

export function createTranslator(language?: AppLanguage) {
  const locale = resolveLocale(language)
  return {
    locale,
    t: (key: MessageKey, values?: Record<string, string | number>) => translate(locale, key, values),
  }
}
